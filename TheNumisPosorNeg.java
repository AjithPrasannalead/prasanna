public class TheNumisPosorNeg {

	public static void main(String[] args) {

		
int number = 35;
		
		if (number>0) {
			System.out.println("Number is positive");     // This condition always true 
		}
			
			else if (number < 0) {
				System.out.println("Number is negative");  // This condition always false
				
			}
		
			else {
				System.out.println("The number is neither positive nor negative"); 
				
			
			
		}

	}

}
